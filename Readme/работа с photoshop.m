Слои
F7 - Показать или скрыть панель слоёв
Shift + Ctrl + N - Создать новый слой 
Ctrl + J Создать -  слой методом копирования
Shift + Cmd + J - Создать слой методом вырезания
Shift + Ctrl + E  - Объединить видимые слои
Shift + Ctrl + ] - Сделать выбранный слой самым верхним
Shift + Ctrl + [ - Сделать выбранный слой самым нижним
Ctrl + ] Поднять выбранный слой на один уровень
Ctrl + [ - Опустить выбранный слой на один уровень
Ctrl + E - Объединить выделенные слои 
Alt + ] - Выделить слой на уровень выше
Alt + [ - Выделить слой на уровень ниже
Ctrl + щелчок - Создать новый слой под текущим. По иконке нового слоя.
Alt + Delete  - Заполнить слой верхним цветом
Ctrl + Delete - Заполнить слой нижним цветом
Открыть окно «Стиль слоя»: двойной щелчок левой кнопкой мыши по слою
Shift + Ctrl + C - Скопировать все слои в выделенной области в буфер обмена

На верхней панели отображаються текущие значения фигуры. И не во всех случаях можно их поменять.
Нужно открыть свойства фигуры если этой панели нет справа выше панели СЛОИ тогда зайти Окно->Свойства.  
Линейка - [Ctrl + R]
Прозрачность - Внизу справа в СЛОИ
Перемещая слои меняеться их порядок
Кисть - [B] Рисует на слое любыми цветами, но на маске этот инструмент работает только с черным и белым цветоми.
            чёрный на маске делает её прозрачной, белый возвращает рисунок
Закинуть картинку или передащить или "Поместить встроенные" "Поместить связанные"
Цвет - [Ctrl + U] или Изображение->Коррекция->Цветовой тон/Насыщеность
Востанавливающая кисть - [J] Может убрать элементы. Мелкие незаметно убирает.
Изменить размер - [Ctrl + T]
Быстрое выделение - [W] выделить любой формы участок и скорректирова. 
                    Удерживать Alt что бы наоборот убирать участки выделения 
Волшебная палка - [W] Выделяет участок одного цвета. (типа как заливка в pain только с выделением)
